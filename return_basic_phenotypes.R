return_basic_phenotypes <- function(merged_data){
  ####################################################################################################################################
  #--------------------------------------------------------------------------------------------------------------------------
  #CD68 cytoplasm
  merged_data$thres__CD68__cyto_mean <- ifelse(( merged_data$Cytoplasm.CD68.Mean.Normalized.expression > 4.5 ), 1, 0)
  #--------------------------------------------------------------------------------------------------------------------------
  #--------------------------------------------------------------------------------------------------------------------------
  #CD4 entire
  merged_data$thres__CD4__entire_mean <- ifelse(( merged_data$Entire.Cell.CD4.Mean.Normalized.expression > 5 ), 1, 0)
  #--------------------------------------------------------------------------------------------------------------------------
  #--------------------------------------------------------------------------------------------------------------------------
  #PDL1 cytoplasm    
  merged_data$thres__PDL1__cyto_mean_unique <- ifelse(( merged_data$Cytoplasm.PD.L1.Mean.Normalized.expression > 0.6 ), 1, 0)
  #--------------------------------------------------------------------------------------------------------------------------
  #--------------------------------------------------------------------------------------------------------------------------
  #CD8 cytoplasm
  merged_data$thres__CD8__cyto_mean <- ifelse(( merged_data$Cytoplasm.CD8.Mean.Normalized.expression > 3.5 ), 1, 0)
  #--------------------------------------------------------------------------------------------------------------------------
  #--------------------------------------------------------------------------------------------------------------------------
  #FoxP3 nucleus
  merged_data$thres__FoxP3__nucleus_mean_unique <- ifelse(( merged_data$Nucleus.FoXP3.Mean.Normalized.expression > 10 ), 1, 0)
  #--------------------------------------------------------------------------------------------------------------------------
  #--------------------------------------------------------------------------------------------------------------------------
  #PD1 cytoplasm  
  merged_data$thres__PD1__cyto_mean_unique <- ifelse(( merged_data$Cytoplasm.PD.1.Mean.Normalized.expression > 10 ), 1, 0)
  #--------------------------------------------------------------------------------------------------------------------------
  #panCK cytoplasm
  merged_data$thres__PanCK__cyto_mean_unique <- ifelse(( merged_data$Cytoplasm.PanCK.Mean.Normalized.expression > 7.5 ), 1, 0)
  --------------------------------------------------------------------------------------------------------------------------
  
  return(merged_data)
}
  